package vo;

public class LeavingVO {
	private int l_idx;
	private int m_ages;
	private String m_reason;
	private String regidate;
	private String id;
	public LeavingVO() {
		
	}
	public int getL_idx() {
		return l_idx;
	}
	public void setL_idx(int l_idx) {
		this.l_idx = l_idx;
	}
	
	public int getM_ages() {
		return m_ages;
	}
	public void setM_ages(int m_ages) {
		this.m_ages = m_ages;
	}
	public String getM_reason() {
		return m_reason;
	}
	public void setM_reason(String m_reason) {
		this.m_reason = m_reason;
	}
	
	public String getRegidate() {
		return regidate;
	}
	public void setRegidate(String regidate) {
		this.regidate = regidate;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	
	
	
	
}
